def ports = new File("maridSite.id").readLines("UTF-8");
def socket = new DatagramSocket();
socket.connect(InetAddress.loopbackAddress, ports[0] as int);
def selector = ports[1].getBytes("UTF-8");
def packet = new DatagramPacket(selector, selector.length);
socket.send(packet);
